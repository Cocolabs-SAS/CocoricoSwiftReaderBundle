namespace Cocorico\SwiftReaderBundle\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Loader\YamlFileLoader;
use Symfony\Component\HttpKernel\DependencyInjection\Extension;

class CocoricoSwiftReaderExtension extends Extension
{
    /**
     * {@inheritDoc}
     */
    public function load(array $configs, ContainerBuilder $container)
    {
        $configuration = new Configuration();
        $config = $this->processConfiguration($configuration, $configs);

        $loader = new YamlFileLoader($container, new FileLocator(__DIR__ . '/../Resources/config'));
        $loader->load('collector.yml');
        $loader->load('listener.yml');
        $loader->load('manager.yml');

        if (isset($config['path'])) {
            $definition = $container->getDefinition('cocorico.swift_reader.message_manager');
            $definition->addMethodCall('setPath', array($config['path']));
        }
    }
}
